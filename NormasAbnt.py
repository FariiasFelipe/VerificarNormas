def verificar_atualizacoes_abnt():
    url = 'https://abnt.org.br/'

    response = requests.get(url)

    if response.status_code == 200:
        soup = BeautifulSoup(response.content, 'html.parser')

        # Procura pelas últimas atualizações
        atualizacoes_normas = soup.find('ul', class_='latest-news-list')

        if atualizacoes_normas:

            normas_atualizadas = atualizacoes_normas.find_all('li')

            normas = []

            for norma in normas_atualizadas:
                norma_texto = norma.text.strip()

                if norma_texto in normas_abnt:
                    normas.append(norma_texto)

            return normas
        else:
            return []
    else:
        print(f'Erro ao acessar as normas da ABNT. Código de status: {response.status_code}')
        return None


